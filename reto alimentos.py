#Reto- evaluación de alimentos.
print ("Aqui evaluaremos los contenidos de azúcar, grasa y sodio de tus alimentos")
print("Porfavor, inserta en gramos el contenido de grasa y azucar")

grasa = float (input(""))
azucar = float (input(""))

if grasa <= 5 and azucar <= 10:
  print("Tu alimento es bajo en grasa y azucar")

print ("Ingresa en gramos el contenido de sodio")
sodio = float (input(""))
  
if sodio <= 100:
  print("Tu alimento es bajo en sodio")
  
else:
  print("Considera las advertencias nutricionales de tu alimento")

  
